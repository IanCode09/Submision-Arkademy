/*
Tipe data di JavaScript adalah :
1. Number => Tipe data yang berbentuk bilangan bulat
2. String => Tipe data yang merepresentasikan data yang bersifat textual
3. Boolean => Hanya memiliki 2 kondisi yaitu True dan False
4. Null => Nilai data kosong/tidak ada nilai sama sekali
5. Undefined => Variabel yang belum didefenisikan atau tidak diberi nilai
*/

// Tipe data Number di JavaScript
const tanggalLahir = 23
const tahunLahir = 1995
let age = 25

// Tipe data String di JavaScript
const namaLengkap = 'Citra Kemurnian Lombu'
const tempatLahir = 'Gunungsitoli'
let alamat = 'Bogor'

// Tipe data Boolean
let kerja = false
let nikah = false
let kuliah = true


const angkaPertama = 50
const angkaKedua = 10
let total

console.log(`Angka Pertama = ${angkaPertama}`);
console.log(`Angka Kedua = ${angkaKedua}`);

//Penjumlahan
total = angkaPertama + angkaKedua
console.log(`Hasil Penjumlahan = ${total}`);

//Pengurangan
total = angkaPertama - angkaKedua
console.log(`Hasil Pengurangan = ${total}`);

//Pembagian
total = angkaPertama / angkaKedua
console.log(`Hasil Pembagian = ${total}`);

//Perkalian
total = angkaPertama * angkaKedua
console.log(`Hasil Perkalian = ${total}`);



