/*  
========================================= SOAL 2 ============================================
CSS Ruleset :

p { color:red; }

1. Selector
Selector adalah bagian CSS untuk merujuk elemen HTML yang ingin dikenakan style. Ada beberapa jenis selector yang dapat digunakan untuk memudahkan pekerjaan styling pada CSS, seperti selector element, selector class, dan selector id.

2. Declaration
Suatu peraturan seperti color: red; yang menentukan properti mana dari elemen yang ingin diberi style.

3. Property
Cara memberikan style terhadap elemen HTML (contoh seperti diatas, color adalah properti dari {elemen {htmlelement("p")}}.) Di dalam CSS, kita bisa memilih properti mana saja yang ingin digunakan.

4. Property Value
Sebelah sisi kanan properti setelah tanda titik dua, kita memiliki nilai properti, yang memilih satu dari banyak kemungkinan penampilan untuk properti yang diberikan. 
*/ 


/*
========================================= SOAL 3 ============================================

CSS Specificity menentukan seberapa spesifik kah sebuah aturan pada CSS. Jika ada 2 atau lebih aturan pada sebuah elemen yang sama, maka aturan paling spesifiklah yang akan dipakai oleh browser.

Nilai specificity pada CSS bisa dihitung sbb:
1. Element Selector, yaitu selector yang berupa tag-tag html, contohnya div, ul, li, a
nilai specificity = 1 (0,0,1)
2. Class Selector, yaitu selector yang diawali dengan tanda titik, contohnya .menu untuk elemen dengan class="menu"
nilai specificity = 10 (0,1,0)
3.  ID Selector, yaitu selector yang diawali dengan tanda #, contohnya #sidebar untuk elemen dengan id="sidebar"
nilai specificity = 100 (1,0,0)

*/

